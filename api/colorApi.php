<?php

function getColor($itemName) {

    if ($itemName === 'Cod') {
        return '0xc7ad95';
    } elseif ($itemName === 'Salmon') {
        return '0xdb8686';
    } elseif ($itemName === 'Pufferfish') {
        return '0xffc670';
    } elseif ($itemName === 'Fishing rod') {
        return '0x422d0e';
    } elseif ($itemName === 'Ender pearl') {
        return '0x349988';
    } elseif ($itemName === 'Water bucket') {
        return '0x234fcc';
    } elseif ($itemName === 'Diamond') {
        return '0x47eaff';
    } elseif ($itemName === 'Furnace') {
        return '0x545454';
    } elseif ($itemName === 'Cake') {
        return '0xffcca6';
    } elseif ($itemName === 'Gold') {
        return '0xffd447';
    } elseif ($itemName === 'Daisy') {
        return '0xffffdb';
    } elseif ($itemName === 'Iron') {
        return '0xc4c4c4';
    } elseif ($itemName === 'Poppy') {
        return '0xd10000';
    } elseif ($itemName === 'Coal') {
        return '0x171717';
    } elseif ($itemName === 'Apple') {
        return '0x9e0000';
    } elseif ($itemName === 'Sweet berries') {
        return '0x9e0000';
    } elseif ($itemName === 'Cobblestone') {
        return '0x6e6e6e';
    } elseif ($itemName === 'Snowball') {
        return '0xd3f0ec';
    } elseif ($itemName === 'Egg') {
        return '0xd4c2a1';
    } elseif ($itemName === 'Dirt') {
        return '0x402d2d';
    } elseif ($itemName === 'Planks') {
        return '0xb8915c';
    } elseif ($itemName === 'Stick') {
        return '0x4a2c01';
    } elseif ($itemName === 'String') {
        return '0xd6d6d6';
    } elseif ($itemName === 'Green') {
        return '0x00FF00';
    } else {
        return '0x212121';
    }
}